# Section 3: The else Block
# Example: IPL match ticket availability

match_tickets_available = False

if match_tickets_available:
    print("Ticket booked! Enjoy the match.")
else:
    print("Sorry, tickets are sold out. Join the waitlist.")

# Key rule: only ONE of the two blocks (if / else) will ever run.
# They never both execute, and they never both skip.
